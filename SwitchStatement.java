class SwitchStatement
{
public static void main(String args[])
{
int a=10,b=30,c;
switch(5)
{
case 1: c=a+b;
System.out.println(c);
break;
case 2: c=a-b;
System.out.println(c);
break;
case 3: c=a*b;
System.out.println(c);
break;
case 4: c=a/b;
System.out.println(c);
break;
case 5: c=a%b;
System.out.println(c);
break;
case 6: c=a&b;
System.out.println(c);
break;
case 7: c=a|b;
System.out.println(c);
break;
case 8: c=a^b;	
System.out.println(c);
break;
default:System.out.println("doesnt exist");
}
}
}

